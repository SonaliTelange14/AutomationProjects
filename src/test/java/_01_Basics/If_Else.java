package _01_Basics;

public class If_Else {

	public static void main(String[] args) {
int num=1010;

if(num%2==0)
{
	System.out.println("num is even number");
}
else if(num%4==0)
{
	System.out.println("num is even number");
}
else if(num%6==0)
{
System.out.println("num is even number");	
}
else
{
	System.out.println("default");
}

	}

}
